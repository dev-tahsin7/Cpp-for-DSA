#include <bits/stdc++.h>
using namespace std; 

int main(){
    // String Element Access 
    string s = "Tahsin"; 
    cout << s[1] << endl; // s[i] -> access the ith index
    cout << s.at(2) << endl; // same here 
    cout << s.back() << endl; // access the last element 
    cout << s.front() << endl; // access the first element 
    
}